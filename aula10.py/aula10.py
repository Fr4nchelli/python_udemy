# Operações condicionais
# usadas para controlar melhor o fluxo do código, evitando erros e tornando ele mais fluido
# if (se) - elif (se não se) - else (se não)
entrada = input('Voce quer "entrar" ou "sair"? ')

if entrada == 'entrar':
    print('Você entrou no sistema')
elif entrada == 'sair':
    print('Você saiu do sistema')
else:
    print('Entrada invalida')
    
