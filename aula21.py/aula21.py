# Laço for/in

# O laço while é comumente usado quando não sabemos a quantidade de repetições que precisamos
# Já o laço for for é utilizado quando sabemos quantas repetições precisamos

texto = 'Python'

for letra in texto:
    print(letra)

# Laço for/range
# range -> (start, stop, step)

numeros = range(5, 10)

for numero in numeros:
    print(numero)